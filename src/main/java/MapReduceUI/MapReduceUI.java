    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        RSSFeedParser parser = new RSSFeedParser(UrlTextField.getText());
        feed = parser.readFeed();
        TotalLabel.setText(Integer.toString(feed.getMessages().size()));
        for(FeedMessage msg: feed.getMessages())
        {
        FeedTextArea.append(msg.getTitle() + System.getProperty("line.separator"));
        }
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

