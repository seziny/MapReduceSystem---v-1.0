        Action pasteAction = UrlTextField.getActionMap().get("paste");
        Action undoAction = UrlTextField.getActionMap().get("undo");
        Action selectAllAction = UrlTextField.getActionMap().get("selectAll");

      
        PopUpMenu.add (undoAction);
        PopUpMenu.addSeparator();
