    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        keywords = GetKeywords(KeyWordsTextField.getText());
        FilteredFeedTextArea.setText(null);
        MRClient mc = new MRClient();
        try {
            filteredFeeds= mc.Filter(feed,keywords);
            TotalFilteredLabel.setText(Integer.toString(filteredFeeds.size()));
            for(FeedMessage msg: filteredFeeds.keySet())
            {
            FilteredFeedTextArea.append(msg.getTitle() + System.getProperty("line.separator"));
            } 
            
        } catch (Exception ex) {
            Logger.getLogger(MapReduceUI.class.getName()).log(Level.SEVERE, null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

